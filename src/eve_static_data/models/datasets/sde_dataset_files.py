class DerivedDatasetFiles(StrEnum):
    MARKET_PATHS = "market_paths.json"
    NORMALIZED_EVE_TYPES = "normalized_eve_types.json"
    NORMALIZED_EVE_TYPES_PUBLISHED = "normalized_eve_types_published.json"

    def localized(self, language_code: str) -> str:
        """Return the filename for the localized version of this dataset."""
        return f"{self.value[:-5]}-{language_code}.json"


